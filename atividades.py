def quadrado(lado):
    area=lado**2
    return area
print(quadrado(30))

def reajuste (salario):
    novo=salario+salario*0.15
    return novo
print(reajuste(1000))  

def triangulo(base,altura):
    resultado=(base*altura/2)
    return resultado
print(triangulo(2,6))

def conversor(c):
    resposta=(9*c+160)/5
    return resposta
print(conversor(100))

def equaçao(y,x):
    resposta=(x,y)
    return resposta
print(equaçao (10,25))
    
